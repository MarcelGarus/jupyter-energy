def prep_query_results(reality: Iterable[QueryKapitelResult], sort=False) -> \
        Union[Tuple[List[List[Tuple[str, int]]], List[BibelKapitel], List[str]],
              Tuple[List[List[Tuple[str, int]]], List[BibelKapitel], List[str], List[float]]]:
    reality = list(reality)
    if sort:
        reality.sort(key=lambda r: (r.rank_score, r.as_reference()), reverse=True)

    reality_hits = []
    reality_kapitel = []
    reality_kapitel_str = []
    scores = []
    for result in reality:
        reality_hits.append(result.hits)
        reality_kapitel.append(result.kapitel)
        reality_kapitel_str.append(result.as_reference())
        scores.append(result.rank_score)

    if sort:
        return reality_hits, reality_kapitel, reality_kapitel_str, scores
    else:
        return reality_hits, reality_kapitel, reality_kapitel_str


